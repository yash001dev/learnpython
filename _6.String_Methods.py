#String Methods
course='Python For Beginners'
print(len(course))
print(f'Upper Case String:{course.upper()}')
print(f'Lower Case String:{course.lower()}')
print(f"First Occurence String For Particular Letter:{course.find('B')}")#It is Case Sensitive
print(f"Replace Of String:{course.replace('Beginners','Absolute Beginners')}")
print(f"Checking Wheather String is Present Or Not:{'python' in course}")#It is Case Sensitive
 

  


