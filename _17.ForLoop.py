#For Loops

print("Iteration For Range Of Names.")
for item in ['Mosh','John','Yash','Adi']:
    print(item)
print("Iteration For Range Of Number.")
for item in range(10):
    print(item)
print("Iteration For Specific Range Of Number.")
for item in range(5,10):
    print(item)
print("Iteration For Specific Range Of Number With Step Count.")
for item in range(1,10,2):
    print(item)

